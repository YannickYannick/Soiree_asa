    if not csrf_raw and ALLOWED_HOSTS:
        csrf_raw = ','.join(
            h for h in ALLOWED_HOSTS
            if h not in ('localhost', '127.0.0.1', '*')
        )
    CSRF_TRUSTED_ORIGINS = _normalize_csrf_origins(csrf_raw)
    SECURE_PROXY_SSL_HEADER = ('HTTP_X_FORWARDED_PROTO', 'https')

