def _normalize_csrf_origins(raw_value):
    """Ajoute https:// si l'origine n'a pas de schéma (requis Django 4+)."""
    origins = []
    for origin in raw_value.split(','):
        origin = origin.strip()
        if not origin:
            continue
        if '://' not in origin:
            origin = f'https://{origin}'
        origins.append(origin)
    return origins


